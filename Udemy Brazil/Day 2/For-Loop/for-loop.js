for (i = 10; i <= 20; i++) {
  console.log('Número ', i);
}

// O loop for é uma estrutura de controle de fluxo que permite executar repetidamente um bloco de código enquanto uma condição específica for verdadeira.

// A inicialização (i = 10) define onde o loop começará. Neste caso, começará com i igual a 10.

// A condição de continuação (i <= 20) é verificada antes de cada iteração do loop. Se for verdadeira, o loop continua. Se for falsa, o loop termina. Aqui, o loop continuará enquanto i for menor ou igual a 20.

// O incremento (i++) é executado após cada iteração do loop. Geralmente, é usado para aumentar ou diminuir o valor da variável de controle (neste caso, i). Aqui, incrementamos i em 1 a cada iteração.

// Dentro do loop, a cada iteração, o console.log imprime o valor atual de i, junto com a string 'Número'.
