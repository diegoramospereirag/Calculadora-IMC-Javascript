i = 1; // Inicializa a variável i com o valor 1

do {
  console.log('Numero ', i); // Imprime o valor atual de i, junto com a string 'Numero'
  i++; // Incrementa i em 1
} while (i <= 10); // Repete o bloco de código enquanto a condição (i <= 10) for verdadeira

// O loop do-while é semelhante ao loop while, mas a diferença é que o bloco de código dentro do loop é executado pelo menos uma vez, mesmo se a condição for falsa na primeira verificação.

// Neste caso, o bloco de código dentro do loop é executado pelo menos uma vez porque a condição é verificada após a execução do bloco de código.

// Após a primeira execução do bloco de código, a variável i é incrementada em 1 a cada iteração.

// O loop continua a ser executado até que a condição (i <= 10) seja falsa.
