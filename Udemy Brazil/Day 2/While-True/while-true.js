i = 1; // Inicializa a variável i com o valor 1

while (i <= 30) {
  // Enquanto i for menor ou igual a 30
  console.log('Numero ', i); // Imprime o valor atual de i, junto com a string 'Numero'
  i++; // Incrementa i em 1
}

// O loop while é outra estrutura de controle de fluxo que permite executar repetidamente um bloco de código enquanto uma condição específica for verdadeira.

// No caso deste loop while, a condição é verificada antes de cada iteração. Se for verdadeira, o bloco de código dentro do loop é executado. Se for falsa, o loop é encerrado.

// Como a variável i é inicializada fora do loop, ela pode ser reutilizada em várias iterações do loop.

// Dentro do loop, o console.log imprime o valor atual de i, junto com a string 'Numero', e depois a variável i é incrementada em 1 a cada iteração.

// Sem o incremento (i++), o loop ficaria preso em uma iteração infinita, imprimindo repetidamente 'Numero 1', pois a condição (i <= 30) nunca seria falsa.
